				foreach (var field in e.members.OrderBy(i => i.Name))
				{
					sb1.AppendLine($$"""
								{{(isFirst ? "" : "else ")}}if (reader.ValueTextEquals("{{GetStringName(field, pascalCase)}}"u8))
